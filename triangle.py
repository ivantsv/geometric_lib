def area(a, h):
    '''Принимает число a - длину стороны треугольника и число h - длину высоты, опущенной на эту сторону.
    Возвращает площадь треугольника.'''
    return a * h / 2

def perimeter(a, b, c):
    '''Принимает числа a, b, c - стороны треугольника. Возвращает его периметр.'''
    return a + b + c
